        <h2>AIoThings proposition</h2>

        <p class=MsoNormal><o:p>&nbsp;</o:p></p>

        <p class=MsoNormal>AIoThings is a productivity tool to accelerate this IoT
        application development process. But what makes it better than other solutions
        introducing from Amazon, Google or IBM? </p>

        <p class=MsoNormal>Aiothings targets on a highly integrated tool/service to
        help clients accomplish their tasks, by flow-based programming, of modularized
        building blocks that include microservices and devices - things. </p>

        <p class=MsoNormal><o:p>&nbsp;</o:p></p>

        <p class=MsoNormal>Actually Aiothings is built on AWS cloud, at the same time,
        provides cloud-agnostic model to leverage strengths from other cloud platforms.</p>

        <p class=MsoNormal><o:p>&nbsp;</o:p></p>

        <p class=MsoNormal>Its design concept following the principle includes:</p>

        <p class=MsoNormal><o:p>&nbsp;</o:p></p>

        <ul style="list-style-type:disc">
          <li>Achieve simplicity, by tradeoffs of flexibility.</li>
          <br />
          <ul style="list-style-type:circle">
            <li>Developer is allowed to concentrate on task flow itself, no need to take care much on
                administration works, such as cloud resource management, various interfaces,
                and user credentials.</li>
          </ul>
          <br />
          <li>Fast delivery of customer solutions, including web/mobile apps, end-to-end service, and IoT device support. </li>
          <br />
          <ul style="list-style-type:circle">
            <li>Positioning as SaaS (Software as a Service) than PaaS (Platform as a Service).</li>
            <br />
            <li>Focus on direct deliverables to clients, including reference designs, prototyping.</li>
          </ul>
          <br />
          <li>A unified interface allows direct communication between IoT edges and cloud services (or microservices).</li>
          <br />
          <ul style="list-style-type:circle">
            <li>Inspired by MQTT subscribe/publish model as the message bus.</li>
            <br />
            <li>Support message bus for serverless cloud services.</li>
          </ul>
          <br />
          <li>Compliance to highest standard of security</li>
          <br />
          <ul style="list-style-type:circle">
