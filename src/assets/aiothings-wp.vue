        <h2>Edge computing</h2>

        <p class=MsoListParagraphCxSpFirst style='margin-left:0in;mso-add-space:auto'><o:p>&nbsp;</o:p></p>

        <p class=MsoListParagraphCxSpMiddle style='margin-left:0in;mso-add-space:auto'>Aiotings
        is designed for rapid prototyping and user-friendly usage. However, for the practical
        reasons, examples of them are: a hypothesis resulted from machine learning
        algorithm can be transferred to edges, or, a pre-processing of IoT data by
        edges before sending them to cloud to reduce data traffics. </p>

        <p class=MsoListParagraphCxSpMiddle style='margin-left:0in;mso-add-space:auto'><o:p>&nbsp;</o:p></p>

        <p class=MsoListParagraphCxSpMiddle style='margin-left:0in;mso-add-space:auto'>For
        those advanced needs, Docker container provides an alternative to implement a
        microservice, provides a lightweight virtual environment capable to run in
        various edge device configurations. Furthermore, Kubernetes, an open-source
        container-orchestration system is the companion tool for automating deployment
        and scaling of the microservices. </p>

        <p class=MsoListParagraphCxSpMiddle style='margin-left:0in;mso-add-space:auto'><o:p>&nbsp;</o:p></p>

        <p class=MsoListParagraphCxSpMiddle style='margin-left:0in;mso-add-space:auto'>Resin.io
        is an example of Docker based edge-computing platform (is open-source too).
        Resin uses its resinOS to fully control edge devices, provides a thorough
        Docker environment.<span style="mso-spacerun:yes">&nbsp;&nbsp; </span></p>

        <p class=MsoListParagraphCxSpMiddle style='margin-left:0in;mso-add-space:auto'><o:p>&nbsp;</o:p></p>

        <p class=MsoListParagraphCxSpMiddle style='margin-left:0in;mso-add-space:auto'>Again,
        the implementation of edge computing over Docker can be expensive for a starter,
        as users need to be familiar with the Docker, OS, and orchestration system technologies
        in order to obtain an optimized result. For a rapid development based on
        'small' microservices, Aiothings combined with Node-RED is able to provide the
        same degree of deployment and scaling performance.<span
        style="mso-spacerun:yes">&nbsp; </span></p>

        <p class=MsoListParagraphCxSpMiddle style='margin-left:0in;mso-add-space:auto'><o:p>&nbsp;</o:p></p>

