        of more shared users.</li>
        <br />
        <li>Each lambda service is accessible from Node-RED
        IoT flow and web/mobile apps, through a common lightweight interface pattern.</li>
        <br />
        <li>The traffic can be watched through monitoring
        messages, a streamlined support to the pay-per-usage charging model. </li>
