        <h3>AWS IoT as the cloud backbone</h3>

        <p class=MsoListParagraphCxSpFirst style='margin-left:0in;mso-add-space:auto'><o:p>&nbsp;</o:p></p>

        <p class=MsoListParagraphCxSpMiddle style='margin-left:0in;mso-add-space:auto'>AWS
        IoT is the largest cloud service provider, offers tremendous services and
        development tools from cloud side, and many of them support industrial
        standards. The largest benefit gained from using AWS is its scalability.<span
        style="mso-spacerun:yes">&nbsp; </span>Also, it provides in-depth development
        frameworks; let IoT applications easily extend to other functionalities, such
        as database, security, machine learning, mobile application, and notifications.
        As AWS supports broad ranges of applications, in addition to IoT, it offers
        high level of flexibility for application design. But sometimes it is too
        flexible that, this characteristic often leads to a great overhead for
        designers new to AWS. </p>

        <p class=MsoListParagraphCxSpMiddle style='margin-left:0in;mso-add-space:auto'><o:p>&nbsp;</o:p></p>

        <p class=MsoListParagraphCxSpMiddle style='margin-left:0in;mso-add-space:auto'>In
        Aiothings, a simplified model regulates this flexibility, trying to accelerate
        the rapid prototyping to production process. </p>

        <p class=MsoListParagraphCxSpLast style='margin-left:0in;mso-add-space:auto'><o:p>&nbsp;</o:p></p>

        <h3>Node-RED for thing logics</h3>

        <p class=MsoNormal><o:p>&nbsp;</o:p></p>

        <p class=MsoListParagraphCxSpFirst style='margin-left:0in;mso-add-space:auto'>Node-RED
        is an open-source flow-based development tool, developed by IBM, and is a
        member of JS Foundation open-source projects. It also owns a large numbers of
        developers and community.</p>

        <p class=MsoListParagraphCxSpMiddle style='margin-left:0in;mso-add-space:auto'>Designer
        uses its visualized editor to design flows that are interconnected by
        input/outputs of nodes. Each node makes a change to the message payload,
        according to the predefined function. User can search for a node type from
        Node-RED repository database, or even designs his/her own node type and
        contributes it to the community. </p>

        <p class=MsoListParagraphCxSpMiddle style='margin-left:0in;mso-add-space:auto'>Node-RED
        normally resides on IoT Gateway over an edge, to have direct interactions with
        local, low-footprint machines and sensors. However, it is also common that,
        Node-RED running as a Docker container, or running on clouds as an instance, depends
        on the actual IoT topology.</p>

        <p class=MsoListParagraphCxSpLast style='margin-left:0in;mso-add-space:auto'><o:p>&nbsp;</o:p></p>

        <h3>Aiothings.com</h3>

        <p class=MsoListParagraphCxSpFirst style='margin-left:0in;mso-add-space:auto'><o:p>&nbsp;</o:p></p>

        <p class=MsoListParagraphCxSpMiddle style='margin-left:0in;mso-add-space:auto'>Aiothings
        has a portal web application for users. Developers can sign-in to use Aiothings
        and its fast prototyping function, without the need of knowing too much cloud
        details.</p>

        <p class=MsoListParagraphCxSpMiddle style='margin-left:0in;mso-add-space:auto'>The
        web app and its backend services provide the necessary administration tasks -
        such as user credentials, security, thing lifecycle, and task monitoring.<span
        style="mso-spacerun:yes">&nbsp; </span>Its application development functions
        let users manage their <b style='mso-bidi-font-weight:normal'>things</b> and
        thing templates (as a form of Node-RED flows), and also <b style='mso-bidi-font-weight:
        normal'>microservices</b> (including machine learning modules) from a browser
        environment. The web app maintains a repository of thing templates and
        microservices as the palettes for fast prototyping. Also, it is capable to
        deploy thing templates to edge devices, through app's user interface, and
        directly edit the flow by launching remote Node-RED editor.</p>

        <p class=MsoListParagraphCxSpMiddle style='margin-left:0in;mso-add-space:auto'>A
        microservice has, at least, a standard interface of MQTT. Because each thing is
        going to connect to Cloud through MQTT protocol, the <span class=SpellE>microservice's</span>
        using MQTT provides a natural integration point to the edge device's flow-based
        programming logics.</p>

