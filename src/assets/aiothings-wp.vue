        <p class=MsoListParagraphCxSpMiddle style='margin-left:0in;mso-add-space:auto'>In
        addition to Aiothings.com, it's codebase can be customized for dedicated
        clients, sample iOS/Android mobile apps are available to clients too. The apps
        communicate with thing devices through MQTT over WebSocket interface, and are secured
        by AWS Cognito and IAM framework. They are highly customizable through AWS
        Amplify mobile app development tools.</p>

