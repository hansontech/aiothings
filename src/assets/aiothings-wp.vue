        <h2>Reusable microservices</h2>

        <p class=MsoListParagraphCxSpFirst style='margin-left:0in;mso-add-space:auto'><o:p>&nbsp;</o:p></p>

        <p class=MsoListParagraphCxSpMiddle style='margin-left:0in;mso-add-space:auto'>To
        proliferate IoT applications benefitted from machine learning, an effective
        approach can be to share and apply market proven machine-learning models for
        new IoT data sources. A repository, even a marketplace to let developers
        discover and deploy the available learning algorithms, based on current IoT
        application attributes, to the new application workflows is supported by Aiothings.</p>

        <p class=MsoListParagraphCxSpMiddle style='margin-left:0in;mso-add-space:auto'><o:p>&nbsp;</o:p></p>

        <p class=MsoListParagraphCxSpMiddle style='margin-left:0in;mso-add-space:auto'>As
        we see from the difference between Fig.1 and Fig.2, Aiothings reduces the complexity
        of each lambda service as microservices. If there are needs to communicate with
        other lambda services or with IoT logics, they all go through MQTT message
